package ExerciciosAula34;

public class ConversorDeVolumes {

	public double litroToCentimetosCubico(double valorA){
		return valorA * 1000;
	}
	public double metroCubicoToLitro(double valorA){
		return valorA * 1000;
	}
	public double metroCubicoToPesCubicos(double valorA){
		return valorA * 35.32;
	}
	public double galaoAmericadoToPolegadaCubica(double valorA){
		return valorA * 231;
	}
	public double galaoAmericanoToLitros(double valorA){
		return valorA * 3.785;
	}
}
