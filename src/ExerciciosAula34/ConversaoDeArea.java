package ExerciciosAula34;

public class ConversaoDeArea {

	public double metroQuadEmPesQuadr(double valorA){
		return 10.76 * valorA;
	}
	public double peQuadradoEmCentimQuad (double valorA){
		return 929 * valorA;
	}
	
	public double milhaQuadrEmAcres(double valorA){
		return 640 * valorA;
	}
	
	public double acreEmPesQuadrados(double valorA){
		return 43560 * valorA;
	}
	
	
	
}
