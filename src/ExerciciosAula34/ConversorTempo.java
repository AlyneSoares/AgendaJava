package ExerciciosAula34;

public class ConversorTempo {
	
	public double minutoEmSegundo(double valorA){
		return valorA * 60;
	}
	public double horaEmMinutos(double valorA){
		return valorA * 60;
	}
	public double diaEmHoras(double valorA){
		return valorA * 24;
	}
	public double semanaEmDias(double valorA){
		return valorA * 7;
	}
	public double mesEmDias(double valorA){
		return valorA * 30;
	}
	public double anoEmDias(double valorA){
		return valorA * 365.25;
	}

	
}
